public class Personel {
    private String isim;
    private int yas;
    private String departman;
    private String pozisyon;
    private int maas;

    public Personel() {
    }

    public Personel(String isim, int yas, String departman, String pozisyon, int maas) {
        this.isim = isim;
        this.yas = yas;
        this.departman = departman;
        this.pozisyon = pozisyon;
        this.maas = maas;
    }

}
